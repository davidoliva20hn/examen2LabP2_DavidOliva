    private void jButtonCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCrearMouseClicked
        carro.add(new Carro(jTextFieldMarca.getText(), jTextFieldModelo.getText(), (int) jSpinnerVelocidad.getValue()));
        DefaultComboBoxModel t = (DefaultComboBoxModel) jComboBox1.getModel();
        t.addElement(carro.get(carro.size() - 1));
        t.getSelectedItem();
        jComboBox1.setModel(t);
        DefaultComboBoxModel t2 = (DefaultComboBoxModel) jComboBox2.getModel();
        t2.addElement(carro.get(carro.size() - 1));
        jComboBox2.setModel(t2);
        if (adminCarros.getArchivos().exists())
        {
         adminCarros.cargarArchivo();
        }
        jPanelJuego.setVisible(false);
        jPanelCrearCarro.setVisible(false);
        jPanelSelecionarCarro.setVisible(true);
        adminCarros.getListaCarro().addAll(carro);
        adminCarros.escribirArchivo();
    }//GEN-LAST:event_jButtonCrearMouseClicked

