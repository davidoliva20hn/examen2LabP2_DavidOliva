    public void CargarTodo() {
        ArrayList<Carro> Listcarro = new ArrayList();
        DefaultComboBoxModel t = (DefaultComboBoxModel) jComboBox1.getModel();
        DefaultComboBoxModel t2 = (DefaultComboBoxModel) jComboBox2.getModel();
        if (adminCarros.getArchivos().exists())
        {
            adminCarros.cargarArchivo();
            Listcarro.addAll(adminCarros.getListaCarro());
            for (Carro carro1 : Listcarro)
            {
                t.addElement(Listcarro.get(Listcarro.indexOf(carro1)));
                jComboBox1.setModel(t);
                t2.addElement(Listcarro.get(Listcarro.indexOf(carro1)));
                jComboBox2.setModel(t2);
            }
        }
    }
